    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        int luastanahtersedia, hargaTanah, hargabangunan, ppn, cicilanperbulan, harga, lamacicil;
        luastanahtersedia = Integer.parseInt(luassedia.getText());
        hargaTanah = Integer.parseInt(hargatanah.getText());
        hargabangunan = Integer.parseInt(hargabangun.getText());
        harga = luastanahtersedia * hargaTanah + hargabangunan ;
        ppn = (int) (0.1 * harga);
        hargatotal.setText(String.valueOf(harga));
        Ppn.setText(String.valueOf(ppn));
        
        if(hargatotal.getText()==  "346000000"){
            dp.setText("9000000");
            lamacicilan.setText("12");
        }
        
        else {
            dp.setText("7000000");
            lamacicilan.setText("12");
        }
        lamacicil = Integer.parseInt(lamacicilan.getText());
        cicilanperbulan = (int)(harga + ppn - Integer.parseInt(dp.getText()) / lamacicil);
        cicil.setText(String.valueOf(cicilanperbulan));
        
        
        
    }//GEN-LAST:event_jButton2ActionPerformed

