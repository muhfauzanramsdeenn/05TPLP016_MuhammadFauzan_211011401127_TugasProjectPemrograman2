    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        String tiperumah;
        if(tipe36.isSelected()){
            tiperumah = "Tipe - 36";
        }
        else if (tipe54.isSelected()){
            tiperumah = "Tipe - 54";
        }
        else if (tipe45.isSelected()){
            tiperumah = "Tipe - 45";
        } else {
            tiperumah ="";
        }
            
        try {
            String sql = "INSERT INTO rumah VALUES('"+text1.getText()+"',"+"'"+combobox.getSelectedItem()+"',"+"'"+tiperumah+"',"+"'"+luasasli.getText()+"',"+"'"+hargatotal.getText()+"',"+"'"+lamacicilan.getText()+"',"+"'"+cicil.getText()+"')";
            java.sql.Connection conn=(Connection)Koneksi.configDB();
            java.sql.PreparedStatement pst = conn.prepareStatement(sql);
            pst.execute();
            JOptionPane.showMessageDialog(null,"Penyimpanan Data Berhasil");
            
        }catch (Exception e){
            JOptionPane.showMessageDialog(this, e.getMessage());
        }
    }//GEN-LAST:event_jButton1ActionPerformed

